func main() {
	db, err := sql.Open("sqlite3", "./trackcard.db")
	if err != nil {
		log.Fatal(err)
	}
	defer db.Close()

	updated := 0
	for code, chineseName := range airportChineseNames {
		result, err := db.Exec(
			"UPDATE airports SET name = ? WHERE iata_code = ? AND name = name_en",
			chineseName, code,
		)
		if err != nil {
			log.Printf("Failed to update %s: %v", code, err)
			continue
		}
		rowsAffected, _ := result.RowsAffected()
		if rowsAffected > 0 {
			updated++
			fmt.Printf("✓ Updated %s: %s\n", code, chineseName)
		}
	}

	fmt.Printf("\n=== 完成 ===\n")
	fmt.Printf("共更新 %d 个机场的中文名称\n", updated)

	// 统计结果
	var total, withChinese int
	db.QueryRow("SELECT COUNT(*) FROM airports").Scan(&total)
	db.QueryRow("SELECT COUNT(*) FROM airports WHERE name != name_en").Scan(&withChinese)
	fmt.Printf("当前状态: %d/%d 个机场有中文名称 (%.1f%%)\n", withChinese, total, float64(withChinese)/float64(total)*100)
}
